#include "libft.h"

char *ft_strjoin(char const *s1, char const *s2)
{
	char* str;
	size_t len1;
	size_t len2;
	int i;
	int j;
		
	if(!s1 || !s2)
		return NULL;
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	str = (char *)malloc(sizeof(char) * (len1 + len2 + 1));
	
	if (!str)
		return (NULL);

	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		str[i + j] = s2[j];
		j++; 
	}
	
	str[i + j] = '\0';
	return str;
}
